async function update() {
    for (const shop of shops) {
        const htmlContent = template(shop);
        const newTitle = `${shop.name}とは？日本発送や関税・送料を徹底解説`;
        const newSlug = `${shop.slug}-guide`; // Not -japan-guide to avoid breaking existing links

        const { error } = await supabase
            .from('posts')
            .update({
                title: newTitle,
                content: htmlContent
            })
            .eq('slug', newSlug);

        if (error) {
            console.log('❌ Error updating ' + newSlug + ': ' + error.message);
        } else {
            console.log('✅ Success: ' + newSlug);
        }
    }
}

